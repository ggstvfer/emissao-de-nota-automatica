import { createContext, useContext, useState, useEffect } from 'react';
import type { ReactNode } from 'react';
import type { User, Session } from '@supabase/supabase-js';

import supabaseAuthService, { type LoginCredentials, type RegisterData } from '../services/supabaseAuth';
import { type UserProfile } from '../services/userProfileService';
import { supabase } from '../lib/supabase';

interface AuthContextType {
  user: User | null;
  session: Session | null;
  profile: UserProfile | null;
  isAuthenticated: boolean;
  isAdmin: boolean;
  isSuperAdmin: boolean;
  isLoading: boolean;
  login: (credentials: LoginCredentials) => Promise<void>;
  register: (data: RegisterData) => Promise<void>;
  logout: () => Promise<void>;
  resetPassword: (email: string) => Promise<void>;
  refreshProfile: () => Promise<void>;
}

const AuthContext = createContext<AuthContextType | undefined>(undefined);

export function useAuth() {
  const context = useContext(AuthContext);
  if (context === undefined) {
    throw new Error('useAuth must be used within an AuthProvider');
  }
  return context;
}

interface AuthProviderProps {
  children: ReactNode;
}

export function AuthProvider({ children }: AuthProviderProps) {
  const [user, setUser] = useState<User | null>(null);
  const [session, setSession] = useState<Session | null>(null);
  const [profile, setProfile] = useState<UserProfile | null>(null);
  const [isLoading, setIsLoading] = useState(true);
  const [initialized, setInitialized] = useState(false);
  const [profileLoaded, setProfileLoaded] = useState<string | null>(null); // Cache do userId do perfil carregado
  const [loadingTimeout, setLoadingTimeout] = useState<NodeJS.Timeout | null>(null);

  // Fun√ß√£o para carregar perfil do usu√°rio
  const loadUserProfile = async (currentUser: User) => {
    console.log('üöÄ [loadUserProfile] INICIADO para:', currentUser.id);
    console.log('üîç [loadUserProfile] Estado atual:', {
      profileLoaded,
      hasProfile: !!profile,
      isLoading
    });
    
    // Se j√° carregamos o perfil para este usu√°rio, n√£o carregar novamente
    if (profileLoaded === currentUser.id && profile) {
      console.log('‚úÖ [loadUserProfile] Perfil j√° carregado para usu√°rio:', currentUser.id);
      return;
    }
    
    // Evitar chamadas m√∫ltiplas simult√¢neas
    if (profileLoaded === `loading-${currentUser.id}`) {
      console.log('‚è≥ [loadUserProfile] Perfil j√° sendo carregado para usu√°rio:', currentUser.id);
      return;
    }
    
    // Marcar como carregando para evitar chamadas simult√¢neas
    setProfileLoaded(`loading-${currentUser.id}`);
    console.log('üîÑ [loadUserProfile] Iniciando carregamento do perfil para usu√°rio:', currentUser.id);
    
    try {
      console.log('üì° Fazendo query direto para user_profiles...');
      
      // Fazer query direta para evitar conflito com getUser()
      const { data: userProfile, error } = await supabase
        .from('user_profiles')
        .select('*')
        .eq('user_id', currentUser.id)
        .single();
        
      console.log('üìã Resposta da query:', { userProfile, error });
      
      // Verificar se houve erro na query
      if (error) {
        if (error.code === 'PGRST116') {
          console.log('üÜï Perfil n√£o encontrado, criando perfil padr√£o...');
          // Criar perfil diretamente no banco
          const { data: newProfile, error: createError } = await supabase
            .from('user_profiles')
            .insert([{
              user_id: currentUser.id,
              email: currentUser.email || '',
              role: 'user',
              company_name: 'Empresa',
              is_active: true,
              created_at: new Date().toISOString(),
              updated_at: new Date().toISOString()
            }])
            .select()
            .single();
            
          if (createError) {
            console.error('‚ùå Erro ao criar perfil:', createError);
            throw createError;
          }
          
          setProfile(newProfile);
          console.log('‚úÖ Perfil criado com sucesso:', newProfile);
        } else {
          console.error('‚ùå Erro na query do perfil:', error);
          throw error;
        }
      } else if (userProfile) {
        setProfile(userProfile);
        console.log('‚úÖ Perfil encontrado:', userProfile);
      } else {
        console.log('‚ö†Ô∏è Perfil √© null, criando tempor√°rio...');
        // Criar perfil tempor√°rio
        const tempProfile = {
          id: `temp-${currentUser.id}`,
          user_id: currentUser.id,
          email: currentUser.email || '',
          role: 'user' as const,
          is_active: true,
          company_name: 'Empresa',
          created_at: new Date().toISOString(),
          updated_at: new Date().toISOString()
        };
        setProfile(tempProfile);
        console.log('üîß Perfil tempor√°rio criado:', tempProfile);
      }
      
      // Marcar perfil como carregado para este usu√°rio
      setProfileLoaded(currentUser.id);
      console.log('‚úÖ [loadUserProfile] CONCLU√çDO com sucesso para:', currentUser.id);
      
    } catch (error) {
      console.error('‚ùå [loadUserProfile] Erro ao carregar perfil do usu√°rio:', error);
      
      // Se erro na cria√ß√£o, definir perfil padr√£o para n√£o travar
      const fallbackProfile = {
        id: `temp-${currentUser.id}`,
        user_id: currentUser.id,
        email: currentUser.email || '',
        role: 'user' as const,
        is_active: true,
        company_name: 'Empresa',
        created_at: new Date().toISOString(),
        updated_at: new Date().toISOString()
      };
      setProfile(fallbackProfile);
      console.log('üîß [loadUserProfile] Perfil de fallback criado:', fallbackProfile);
      
      // Marcar perfil como carregado mesmo em caso de erro
      setProfileLoaded(currentUser.id);
      console.log('‚úÖ [loadUserProfile] CONCLU√çDO com fallback para:', currentUser.id);
    }
    
    console.log('üèÅ [loadUserProfile] FINALIZANDO fun√ß√£o para:', currentUser.id);
  };

  const refreshProfile = async () => {
    if (user) {
      // For√ßar recarregamento do perfil (limpar cache)
      setProfileLoaded(null);
      await loadUserProfile(user);
    }
  };

  // Timeout de seguran√ßa para garantir que loading nunca trave para sempre
  useEffect(() => {
    const timeout = setTimeout(() => {
      console.warn('‚ö†Ô∏è TIMEOUT DE SEGURAN√áA: For√ßando fim do loading ap√≥s 15 segundos');
      setIsLoading(false);
    }, 15000);
    
    setLoadingTimeout(timeout);
    
    return () => {
      if (loadingTimeout) {
        clearTimeout(loadingTimeout);
      }
    };
  }, [loadingTimeout]);

  useEffect(() => {
    if (initialized) return; // Evitar re-inicializa√ß√£o

    let isInitializing = true;

    // Verificar sess√£o ao inicializar
    const initializeAuth = async () => {
      try {
        const currentSession = await supabaseAuthService.getSession();
        
        if (!isInitializing) return; // Cancelar se componente foi desmontado
        
        setSession(currentSession);
        setUser(currentSession?.user || null);
        
        // Carregar perfil se h√° usu√°rio (apenas na inicializa√ß√£o)
        if (currentSession?.user) {
          try {
            await loadUserProfile(currentSession.user);
          } catch (error) {
            console.error('Erro ao carregar perfil na inicializa√ß√£o:', error);
          }
        }
        
        setInitialized(true);
      } catch (error) {
        console.error('Erro ao inicializar auth:', error);
      } finally {
        if (isInitializing) {
          if (loadingTimeout) {
            clearTimeout(loadingTimeout);
            setLoadingTimeout(null);
          }
          setIsLoading(false);
          console.log('üèÅ [initializeAuth] Loading finalizado na inicializa√ß√£o');
        }
      }
    };

    initializeAuth();

    // Escutar mudan√ßas na autentica√ß√£o (apenas eventos importantes)
    const { data: { subscription } } = supabaseAuthService.onAuthStateChange(
      async (event, session) => {
        // Ignorar evento INITIAL_SESSION para evitar execu√ß√£o dupla
        if (event === 'INITIAL_SESSION') {
          return;
        }
        
        console.log('Auth state changed:', event, session?.user?.id || 'null');
        
        // Apenas processar mudan√ßas reais de estado
        if (event === 'SIGNED_IN' || event === 'SIGNED_OUT' || event === 'TOKEN_REFRESHED') {
          console.log(`üîÑ [onAuthStateChange] Processando evento: ${event}, User ID: ${session?.user?.id || 'null'}`);
          console.log(`üìä [onAuthStateChange] Estado antes:`, { isLoading, initialized, profileLoaded });
          
          setSession(session);
          setUser(session?.user || null);
          
          // Carregar perfil apenas no login, n√£o em refresh
          if (event === 'SIGNED_IN' && session?.user) {
            console.log('üìù Iniciando carregamento do perfil...');
            
            // FOR√áA O LOADING PARA FALSE IMEDIATAMENTE
            console.log('üö® FOR√áA: Definindo isLoading = false ANTES de carregar perfil');
            setIsLoading(false);
            
            try {
              await loadUserProfile(session.user);
              console.log('‚úÖ Perfil carregado com sucesso ap√≥s loading false');
            } catch (error) {
              console.error('‚ùå Erro ao carregar perfil ap√≥s login:', error);
            }
            
            // Limpar timeout se existir
            if (loadingTimeout) {
              clearTimeout(loadingTimeout);
              setLoadingTimeout(null);
            }
          } else if (event === 'SIGNED_OUT') {
            console.log('üëã Usu√°rio saiu, limpando dados...');
            setProfile(null);
            setProfileLoaded(null); // Limpar cache do perfil
            setIsLoading(false);
          } else if (event === 'TOKEN_REFRESHED') {
            console.log('üîÑ Token renovado, finalizando loading...');
            // No refresh, n√£o recarregar perfil, apenas finalizar loading
            setIsLoading(false);
          }
          
          if (!initialized) {
            console.log('üöÄ Marcando como inicializado...');
            setInitialized(true);
          }
        } else {
          console.log(`‚è≠Ô∏è Ignorando evento: ${event}`);
        }
      }
    );

    return () => {
      isInitializing = false;
      subscription.unsubscribe();
    };
  }, [initialized]);

  const login = async (credentials: LoginCredentials) => {
    try {
      setIsLoading(true);
      await supabaseAuthService.login(credentials);
      
      // O evento SIGNED_IN do listener j√° vai processar a sess√£o e usu√°rio
      // Mas garantimos que o loading seja finalizado ap√≥s o login
      
    } catch (error) {
      console.error('Erro no login:', error);
      setIsLoading(false);
      throw error;
    }
  };

  const register = async (data: RegisterData) => {
    try {
      setIsLoading(true);
      await supabaseAuthService.register(data);
      
      // O evento SIGNED_IN do listener j√° vai processar a sess√£o
      // O perfil ser√° criado automaticamente pelo trigger ou pela fun√ß√£o loadUserProfile
      
    } catch (error) {
      console.error('Erro no registro:', error);
      setIsLoading(false);
      throw error;
    }
  };

  const logout = async () => {
    try {
      setIsLoading(true);
      await supabaseAuthService.logout();
      
      // O evento SIGNED_OUT do listener j√° vai limpar os estados
      // N√£o precisamos duplicar a l√≥gica aqui
      
    } catch (error) {
      console.error('Erro no logout:', error);
      setIsLoading(false);
      throw error;
    }
  };

  const resetPassword = async (email: string) => {
    try {
      await supabaseAuthService.resetPassword({ email });
    } catch (error) {
      console.error('Erro ao resetar senha:', error);
      throw error;
    }
  };

  // Computed values baseados no perfil
  const isAdmin = profile ? ['admin', 'super_admin'].includes(profile.role) : false;
  const isSuperAdmin = profile ? profile.role === 'super_admin' : false;

  const value: AuthContextType = {
    user,
    session,
    profile,
    isAuthenticated: !!user,
    isAdmin,
    isSuperAdmin,
    isLoading,
    login,
    register,
    logout,
    resetPassword,
    refreshProfile
  };

  return (
    <AuthContext.Provider value={value}>
      {children}
    </AuthContext.Provider>
  );
}